package com.cooee.wallpaper.data;


public class WallpaperItemInfo
{
	
	private String resid;
	private String resurl;
	private String packname;
	private String size;
	
	public String getResId()
	{
		return resid;
	}
	
	public void setResId(
			String resid )
	{
		this.resid = resid;
	}
	
	public String getResUrl()
	{
		return resurl;
	}
	
	public void setResUrl(
			String resurl )
	{
		this.resurl = resurl;
	}
	
	public String getPackname()
	{
		return packname;
	}
	
	public void setPackname(
			String packname )
	{
		this.packname = packname;
	}
	
	public String getSize()
	{
		return size;
	}
	
	public void setSize(
			String size )
	{
		this.size = size;
	}
}
